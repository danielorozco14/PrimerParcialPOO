public class NumeroHab {
    private String HabS;
    private String HabD;

    public NumeroHab(String HabS, String HabD) {
        this.HabS = HabS;
        this.HabD = HabD;
    }

    public String getHabS() {
        return HabS;
    }

    public void setHabS(String HabS) {
        this.HabS = HabS;
    }

    public String getHabD() {
        return HabD;
    }

    public void setHabD(String HabD) {
        this.HabD = HabD;
    }

    
}
