package primerparcialpoo;

/**
 *
 * @author Roxana
 */
public class Fechas {
    private String fi;
    private String fs;

    public Fechas(String fi, String fs){
        this.fi = fi;
        this.fs = fs;
    }

    public String getFi() {
        return fi;
    }

    public void setFi(String fi) {
        this.fi = fi;
    }

    public String getFs() {
        return fs;
    }

    public void setFs(String fs) {
        this.fs = fs;
    }
    
}
