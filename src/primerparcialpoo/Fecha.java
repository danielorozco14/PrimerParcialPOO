public class Fecha {
    //ATRIBUTOS
    private int Dia;
    private int Mes;
    private int Annio;
    
    //CONSTRUCTOR

    public Fecha() {
        
    }
    
    //SETTER AND GETTER

    public int getDia() {
        return Dia;
    }

    public void setDia(int Dia) {
        this.Dia = Dia;
    }

    public int getMes() {
        return Mes;
    }

    public void setMes(int Mes) {
        this.Mes = Mes;
    }

    public int getAnnio() {
        return Annio;
    }

    public void setAnnio(int Annio) {
        this.Annio = Annio;
    }
    
    
           
    
}
